public String validGST(String gst)
    {
        String result="";
        if(gst.length()==0)
        {
            result="Please Enter the GST Number";
        }
       
        else
        {
           try {
			if (new GSTValidator().validGSTIN(gst))
				result="VALID GST";
			else
				result="INVALID GST";
		} catch (Exception e) {
			e.printStackTrace();
		}
        }
        
        
        
        return result;
    }
